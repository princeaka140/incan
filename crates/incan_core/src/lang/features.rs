/// Broad feature family for docs grouping.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]
pub enum FeatureCategory {
    Syntax,
    TypeSystem,
    Stdlib,
    Interop,
    Testing,
    Async,
    Tooling,
    Libraries,
}

/// Link to an authored reference, how-to, release note, or generated table.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]
pub struct FeatureLink {
    pub label: &'static str,
    pub path: &'static str,
}

/// Product-level language capability descriptor.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]
pub struct FeatureDescriptor {
    pub id: FeatureId,
    pub name: &'static str,
    pub category: FeatureCategory,
    pub since: Since,
    pub introduced_in_rfc: RfcId,
    pub stability: Stability,
    pub activation: &'static str,
    pub summary: &'static str,
    pub canonical_forms: &'static [&'static str],
    pub prefer_over: &'static str,
    pub references: &'static [FeatureLink],
}

macro_rules! links {
    ($(($label:literal, $path:literal)),+ $(,)?) => {
        &[
            $(FeatureLink { label: $label, path: $path }),+
        ]
    };
}

